PHYSICS_ATTENTION = {
    'unstructured': Physics_Attention_Irregular_Mesh,
    'structured_1D': Physics_Attention_Structured_Mesh_1D,
    'structured_2D': Physics_Attention_Structured_Mesh_2D,
    'structured_3D': Physics_Attention_Structured_Mesh_3D
}


class Transolver_block(nn.Module):
    """Transolver encoder block."""

    def __init__(
            self,
            num_heads: int,
            hidden_dim: int,
            dropout: float,
            mlp_ratio=4,
            last_layer=False,
            out_dim=1,
            slice_num=32,
            geotype='unstructured',
            shapelist=None
    ):
        super().__init__()
        self.last_layer = last_layer
        self.ln_1 = nn.LayerNorm(hidden_dim)

        self.Attn = PHYSICS_ATTENTION[geotype](hidden_dim, heads=num_heads, dim_head=hidden_dim // num_heads,
                                               dropout=dropout, slice_num=slice_num, shapelist=shapelist)
        self.ln_2 = nn.LayerNorm(hidden_dim)
        self.mlp = MLP(hidden_dim, hidden_dim * mlp_ratio, hidden_dim, n_layers=0, res=False)
        if self.last_layer:
            self.ln_3 = nn.LayerNorm(hidden_dim)
            self.mlp2 = nn.Linear(hidden_dim, out_dim)

    def forward(self, fx):
        fx = self.Attn(self.ln_1(fx)) + fx
        fx = self.mlp(self.ln_2(fx)) + fx
        if self.last_layer:
            return self.mlp2(self.ln_3(fx))
        else:
            return fx


class Transolver(nn.Module):
    def __init__(self, c_in, c_hidden, slice_num, num_heads, num_layers, dimensionality=2, mlp_ratio=2, dropout=0.):
        super().__init__()
        self.__name__ = 'Transolver'
        self.dimensionality = dimensionality
        
        ## embedding
        self.preprocess = MLP(c_in, c_hidden * 2, c_hidden, n_layers=0, res=False)
        self.placeholder = nn.Parameter((1 / (c_hidden)) * torch.rand(c_hidden, dtype=torch.float))

        ## blocks
        self.blocks = nn.ModuleList([
            Transolver_block(
                num_heads=num_heads, 
                hidden_dim=c_hidden,
                dropout=dropout,
                mlp_ratio=mlp_ratio,
                out_dim=c_hidden,
                slice_num=slice_num,
                last_layer=(_ == num_layers - 1),
                geotype="unstructured",
                shapelist=None)
                for _ in range(num_layers)
        ])

        self.in_dim = c_in
        self.out_dim = c_hidden
        self.initialize_weights()

    def initialize_weights(self):
        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=0.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, (nn.LayerNorm, nn.BatchNorm1d)):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)

    def forward(self, node_features, node_positions, batch_idx, **kwargs):

        batch_size = batch_idx[-1] + 1

        node_features = node_features.reshape(batch_size, -1, self.in_dim)
        node_features = node_features + self.placeholder[None, None, :]

        for block in self.blocks:
            node_features = block(node_features)
            
        return node_features.reshape(-1, self.out_dim)